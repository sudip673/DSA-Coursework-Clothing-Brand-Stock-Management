    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int id = Integer.parseInt(txtId1.getText().trim());
            int quantity = Integer.parseInt(txtQuantity1.getText().trim());
            double price = Double.parseDouble(txtPrice1.getText().trim());

            // Find existing cloth item
            ItemModel existingItem = controller.findById(id);

            if (existingItem == null) {
                JOptionPane.showMessageDialog(this, "No cloth found with ID: " + id, "Update Failed", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Directly update the existing object (no delete needed)
            existingItem.setQuantity(quantity);
            existingItem.setPrice(price);

            // Reload table
            loadTable();

            JOptionPane.showMessageDialog(this, "Cloth updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

            // Clear fields
            txtId1.setText("");
            txtQuantity1.setText("");
            txtPrice1.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter valid numbers for ID, Quantity and Price!", "Input Error", JOptionPane.WARNING_MESSAGE);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {
            // Read ID from text field
            int id = Integer.parseInt(txtId1.getText().trim());

            // Check if cloth item exists
            ItemModel existingItem = controller.findById(id);

            if (existingItem == null) {
                JOptionPane.showMessageDialog(
                        this,
                        "No cloth item found with ID: " + id,
                        "Delete Failed",
                        JOptionPane.ERROR_MESSAGE
                );
                return;
            }

            // Confirm deletion
            int confirm = JOptionPane.showConfirmDialog(
                    this,
                    "Are you sure you want to delete this cloth item?",
                    "Confirm Delete",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }

            // Remove cloth item
            controller.removeById(id);

            // Refresh table
            loadTable();

            JOptionPane.showMessageDialog(
                    this,
                    "Cloth item deleted successfully!",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE
            );

            // Clear only the ID field you actually have
            txtId1.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(
                    this,
                    "Please enter a valid numeric cloth ID.",
                    "Input Error",
                    JOptionPane.WARNING_MESSAGE
            );
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

