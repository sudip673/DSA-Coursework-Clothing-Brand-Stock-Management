    public void loadTable() {
        DefaultTableModel model = (DefaultTableModel) displayTable.getModel();

// Clear table rows
        model.setRowCount(0);

// Get list from controller
        LinkedList<ItemModel> allClothes = controller.getClothList();

// Add rows
        for (ItemModel c : allClothes) {
            model.addRow(new Object[]{
                c.getItemId(),
                c.getName(),
                c.getCategory(),
                c.getQuantity(),
                c.getPrice()
            });
        }

    }

